package com.example.apicalls;

public class ResponseClass {
    private int id;
    private int userId;
    private String title;
    private String body;

    public ResponseClass(int id, int userId, String title, String body) {
        this.id = id;
        this.userId = userId;
        this.title = title;
        this.body = body;
    }

    public int getId() {
        return id;
    }

    public int getUserId() {
        return userId;
    }

    public String getTitle() {
        return title;
    }

    public String getBody() {
        return body;
    }
}
